namespace NetDaemonApps.Models.Enums;

/// <summary>
/// The level enum represents different energy price levels.
/// </summary>

public enum Level
{
    /// <summary>
    /// The none level
    /// </summary>
    None = 0,

    /// <summary>
    /// The low level
    /// </summary>
    Low = 1,

    /// <summary>
    /// The medium level
    /// </summary>
    Medium = 2,

    /// <summary>
    /// The high level
    /// </summary>
    High = 3,

    /// <summary>
    /// The maximum level
    /// </summary>
    Maximum = 4
}
